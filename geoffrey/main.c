#include "main.h"

int	main(void)
{
	DDRB |= (1 << DDB5);
	DDRB |= (1 << DDB0);

	while (1)
	{
		PORTB ^= (1 << PB5);
		PORTB ^= (1 << PB0);
		_delay_ms(100);
	}
	return (0);
}